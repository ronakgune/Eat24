package com.example.poojanshah.recyclerview;

public class ListItem {
    private String head;
    private String desc;

    public ListItem(String head, String desc) {
        this.head = head;
        this.desc = desc;
    }

    public String getHead() {
        return head;
    }

    public String getDesc() {
      //  System.out.println();
        return desc;
    }
}
